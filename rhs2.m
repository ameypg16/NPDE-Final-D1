function Z=rhs2(vertices,qin,pin,nodes)
mk=1/2*det([ones(1,3);vertices']);
qin=qin(nodes);
pin=pin(nodes);
T1=[24 6 6 6 4 2 2 6 4 2]*2*mk/720;
T2=[6 24 6 4 6 6 4 2 2 2]*2*mk/720;
T3=[6 6 24 2 2 4 6 4 6 2]*2*mk/720;
% T22=[4 24 4 2*6 2*6 2*2]*2*mk/120;
% T23=[2 6 6 2*2 2*4 2*2]*2*mk/120;
% T33=[4 4 24 2*2 2*6 2*6]*2*mk/120;
% T21=T12;T32=T23;T31=T13;
S=zeros(10,1);
S(1,1)=qin(1,1)^2*pin(1,1); S(4,1)=(qin(1,1)^2)*pin(1,2)+2*qin(1,1)*qin(1,2)*pin(1,1);
S(2,1)=qin(1,2)^2*pin(1,2); S(5,1)=(qin(1,2)^2)*pin(1,1)+2*qin(1,1)*qin(1,2)*pin(1,2);
S(3,1)=qin(1,3)^2*pin(1,3); S(6,1)=(qin(1,2)^2)*pin(1,3)+2*qin(1,2)*qin(1,3)*pin(1,2);
S(7,1)=(qin(1,3)^2)*pin(1,2)+2*qin(1,2)*qin(1,3)*pin(1,3);
S(8,1)=(qin(1,1)^2)*pin(1,3)+2*qin(1,1)*qin(1,3)*pin(1,1);
S(9,1)=(qin(1,3)^2)*pin(1,1)+2*qin(1,1)*qin(1,3)*pin(1,3);
S(10,1)=2*qin(1,1)*qin(1,2)*pin(1,3)+2*qin(1,2)*qin(1,3)*pin(1,1)+2*qin(1,1)*qin(1,3)*pin(1,2);
Z=zeros(3,1);
Z(1,1)=T1*S;Z(2,1)=T2*S;Z(3,1)=T3*S;